# criando uma tupla de exenplo
minha_tupla = (1, 2, 3, 3, 4, 5,)

print ("minha tupla: ", minha_tupla)

print("minha tupla[0]: ", minha_tupla[0])
print("minha tupla[2]: ", minha_tupla[2])
print("minha tupla[-1]: ", minha_tupla[-1])

# MÃ©todo count
contagem = minha_tupla.count(3)
print("Quntidade de vezes que o elemento 3 aparece: ", contagem)

indice = minha_tupla.index(4)
print("Indice dea primeira ocorrencia do elemento 4: ", indice)